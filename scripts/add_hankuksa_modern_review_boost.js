function updatePages() {
  for (const filePath of PAGE_FILES) {
    const source = read(filePath);
    const { lessons, originalJson } = extractLessons(source);
    if (filePath.includes('/05/')) update05(lessons);
    if (filePath.includes('/06/')) update06(lessons);
    if (filePath.includes('/07/')) update07(lessons);
    updateLessonMeta(lessons);
    write(filePath, replaceLessons(source, originalJson, lessons));
  }
}

