function extractLessons(source) {
  const match = source.match(/const LESSONS = (\[[\s\S]*?\]);\nlet curTab/);
  if (!match) throw new Error('LESSONS block not found');
  return {
    lessons: JSON.parse(match[1]),
    originalJson: match[1],
    prefix: match[0].slice(0, match[0].indexOf(match[1])),
    suffix: ';\nlet curTab'
  };
}

function replaceLessons(source, originalJson, lessons) {
  const pretty = originalJson.includes('\n');
  const nextJson = JSON.stringify(lessons, null, pretty ? 2 : 0);
  let next = source.replace(
    /const LESSONS = \[[\s\S]*?\];\nlet curTab/,
    `const LESSONS = ${nextJson};\nlet curTab`
  );
  next = syncCardSummaries(next, lessons);
  return next;
}

function syncCardSummaries(source, lessons) {
  let next = source;
  lessons.forEach((lesson, idx) => {
    const count = lesson.items?.length || lesson.count || 0;
    const preview = lesson.preview || previewFor(lesson);
    const re = new RegExp(
      `(<div class="card" onclick="startLesson\\(${idx}\\)">[\\s\\S]*?<span class="b-table">)\\d+개(</span>[\\s\\S]*?<div class="card-preview">)[\\s\\S]*?(</div><div class="card-cta">)`,
      'm'
    );
    next = next.replace(re, `$1${count}개$2${preview}$3`);
  });
  return next;
}

function unique(values) {
  const seen = new Set();
  const out = [];
  for (const value of values.filter(Boolean)) {
    const key = String(value).replace(/\s+/g, ' ').trim();
    if (!key || seen.has(key)) continue;
    seen.add(key);
    out.push(key);
  }
  return out;
}

function addKeywords(item, keywords) {
  item.keywords = unique([...(item.keywords || []), ...keywords]);
}

function appendTextOnce(item, field, text) {
  if (!text) return;
  const current = item[field] || '';
  if (current.includes(text.slice(0, Math.min(40, text.length)))) return;
  item[field] = current ? `${current}\n\n${text}` : text;
}

function removeKeyword(item, keyword) {
  const target = compact(keyword);
  item.keywords = (item.keywords || []).filter(value => compact(value) !== target);
}

function replaceTextDeep(value, replacements) {
  if (typeof value === 'string') {
    let out = value;
    for (const [from, to] of replacements) out = out.split(from).join(to);
    return out;
  }
  if (Array.isArray(value)) return value.map(item => replaceTextDeep(item, replacements));
  if (value && typeof value === 'object') {
    for (const key of Object.keys(value)) value[key] = replaceTextDeep(value[key], replacements);
  }
  return value;
}

function compact(value) {
  return String(value || '')
    .normalize('NFKC')
    .toLowerCase()
    .replace(/[()\[\]{}'"`“”‘’.,/·:;!?~\-_\s]/g, '');
}

function addTableRows(item, rows) {
  const table = item.table || [];
  const seen = new Set(table.map(row => compact(Object.values(row).join('|'))));
  for (const row of rows) {
    const key = compact(Object.values(row).join('|'));
    if (seen.has(key)) continue;
    seen.add(key);
    table.push(row);
  }
  item.table = table;
}

function concept({
  title,
  keywords,
  background,
  explanation,
  why = '',
  trap = '',
  table = [],
  connections = [],
  sources = []
}) {
  return {
    title,
    keywords: unique(keywords),
    background,
    explanation,
    why,
    connections,
    trap,
    table,
    sources,
    mnemonic: mnemonicFor(title, keywords, background, why, trap)
  };
}

function mnemonicFor(title, keywords, background, why, trap) {
  const core = keywords.slice(0, 5).join('·');
  return [
    `【왜】 ${background}`,
    `【전개】 ${title}은(는) ${core} 키워드를 한 장면으로 묶어 잡아.`,
    `【결과】 ${why || explanationSummary(title)}`,
    `【문제풀이】 선지에 ${core}가 보이면 먼저 시기·주체·결과를 확인해.`,
    `【오답방지】 ${trap}`
  ].join('\n');
}

function explanationSummary(title) {
  return `${title}은 앞 사건의 결과와 다음 시기 변화가 이어지는 연결 지점이다.`;
}

function ensureItem(lesson, item, afterTitle = '') {
  const existing = (lesson.items || []).find(value => value.title === item.title);
  if (existing) return existing;
  lesson.items ||= [];
  if (afterTitle) {
    const idx = lesson.items.findIndex(value => value.title === afterTitle);
    if (idx >= 0) {
      lesson.items.splice(idx + 1, 0, item);
      return item;
    }
  }
  lesson.items.push(item);
  return item;
}

function previewFor(lesson) {
  const titles = (lesson.items || []).map(item => item.title);
  const shown = titles.slice(0, 3).join(' · ');
  return `스토리형: ${shown}${titles.length > 3 ? ' …' : ''}`;
}

function updateLessonMeta(lessons) {
  for (const lesson of lessons) {
    lesson.concept_titles = (lesson.items || []).map(item => item.title);
    lesson.preview = previewFor(lesson);
    lesson.count = (lesson.items || []).length;
  }
}

